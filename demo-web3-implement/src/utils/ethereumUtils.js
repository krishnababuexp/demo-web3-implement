export const createEthTxn = async (provider, amount, recipientAddress) => {
  const contractAddress = import.meta.env.VITE_DYNEUM_ETH_CONTRACT_ADDRESS;
  const signer = await provider.getSigner();
  const contract = new ethers.Contract(contractAddress, contractABI, signer);
  const amountInEth = amount.toString(); // assume it's already in ETH
  console.log(
    "amountInEth:",
    amountInEth,
    "recipientAddress:",
    recipientAddress,
    "contractAddress:",
    contractAddress
  );
  try {
    const txResponse = await contract.sendEth(recipientAddress, {
      value: parseEther(amountInEth),
    });
    console.log("Waiting for confirmation...");
    // This waits for 1 confirmation (default)
    const receipt = await txResponse.wait();
    console.log("Transaction receipt:", receipt);
    return receipt.hash;
  } catch (error) {
    console.error("Transaction failed:", error);
    throw error;
  }
};
